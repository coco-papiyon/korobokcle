type CopilotCLIProvider struct{}

func NewCopilotCLIProvider() *CopilotCLIProvider {
	return &CopilotCLIProvider{}
}

func (p *CopilotCLIProvider) Run(ctx context.Context, req AIRequest) (AIResult, error) {
	provider := ExternalCLIProvider{
		Name:        "copilot",
		EnvPrefix:   "KOROBOKCLE_COPILOT",
		DefaultBin:  "copilot",
		DefaultArgs: []string{"suggest", "-t", "general", "{{model_flag}}", "{{model}}", "{{prompt}}"},
	}
	return provider.Run(ctx, req)
}


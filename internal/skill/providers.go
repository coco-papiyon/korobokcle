type ExternalCLIProvider struct {
	Name        string
	EnvPrefix   string
	DefaultBin  string
	DefaultArgs []string
}

func (p ExternalCLIProvider) Run(ctx context.Context, req AIRequest) (AIResult, error) {
	executable := firstNonEmpty(os.Getenv(p.EnvPrefix+"_BIN"), p.DefaultBin)
	args, err := loadProviderArgs(p.EnvPrefix+"_ARGS_JSON", p.DefaultArgs)
	if err != nil {
		return AIResult{}, fmt.Errorf("%s provider args: %w", p.Name, err)
	}
	promptInArgs := providerArgsContainPrompt(args)
	args = expandProviderArgs(args, req)

	if _, err := exec.LookPath(executable); err != nil {
		return AIResult{}, fmt.Errorf("%s command %q is not available: %w", p.Name, executable, err)
	}

	workDir := firstNonEmpty(req.WorkDir, req.ArtifactDir, ".")
	cmd := exec.CommandContext(ctx, executable, args...)
	cmd.Dir = workDir

	var stdout bytes.Buffer
	var stderr bytes.Buffer
	cmd.Stdout = &stdout
	cmd.Stderr = &stderr
	if !promptInArgs {
		cmd.Stdin = strings.NewReader(req.Prompt)
	}

	err = cmd.Run()
	result := AIResult{
		Stdout: stdout.String(),
		Stderr: stderr.String(),
		Output: strings.TrimSpace(stdout.String()),
	}

	if result.Output == "" {
		if raw, readErr := os.ReadFile(req.OutputPath); readErr == nil {
			result.Output = string(raw)
		}
	}

	if err != nil {
		return result, fmt.Errorf("%s provider command failed: %w; stderr=%s", p.Name, err, strings.TrimSpace(result.Stderr))
	}
	if strings.TrimSpace(result.Output) == "" {
		return result, fmt.Errorf("%s provider returned empty output", p.Name)
	}
	return result, nil
}

func loadProviderArgs(envName string, defaults []string) ([]string, error) {
	raw := strings.TrimSpace(os.Getenv(envName))
	if raw == "" {
		return append([]string(nil), defaults...), nil
	}

	var args []string
	if err := json.Unmarshal([]byte(raw), &args); err != nil {
		return nil, fmt.Errorf("invalid JSON in %s: %w", envName, err)
	}
	return args, nil
}

func expandProviderArgs(args []string, req AIRequest) []string {
	replacements := map[string]string{
		"{{prompt}}":       req.Prompt,
		"{{model_flag}}":   modelFlag(req.Model),
		"{{model}}":        req.Model,
		"{{work_dir}}":     req.WorkDir,
		"{{artifact_dir}}": req.ArtifactDir,
		"{{output_path}}":  req.OutputPath,
		"{{output_file}}":  filepath.Base(req.OutputPath),
		"{{skill_name}}":   req.SkillName,
	}

	expanded := make([]string, 0, len(args))
	for _, arg := range args {
		value := arg
		for placeholder, replacement := range replacements {
			value = strings.ReplaceAll(value, placeholder, replacement)
		}
		if strings.TrimSpace(value) == "" {
			continue
		}
		expanded = append(expanded, value)
	}
	return expanded
}

func providerArgsContainPrompt(args []string) bool {
	for _, arg := range args {
		if strings.Contains(arg, "{{prompt}}") {
			return true
		}
	}
	return false
}

func firstNonEmpty(values ...string) string {
	for _, value := range values {
		if strings.TrimSpace(value) != "" {
			return value
		}
	}
	return ""
}

func modelFlag(model string) string {
	if strings.TrimSpace(model) == "" {
		return ""
	}
	return "--model"
}

func ProviderFor(name string) (AIProvider, error) {
	switch strings.ToLower(strings.TrimSpace(name)) {
	case "", "mock":
		return NewMockProvider(), nil
	case "copilot":
		return NewCopilotCLIProvider(), nil
	case "codex":
		return NewCodexCLIProvider(), nil
	default:
		return nil, fmt.Errorf("unknown provider %q", name)
	}
}
